#include <stdint.h>
#include <stdio.h>
#include <string.h>


char* toHex(uint8_t vpriv_bytes[],int size){
	
	char buffer [100];
	static char target[2048] = "";
	for(int i =0;i < size; i++){
		sprintf (buffer, "%02X", vpriv_bytes[i]);
        	strcat(target, buffer);
	}
	return target;
}

